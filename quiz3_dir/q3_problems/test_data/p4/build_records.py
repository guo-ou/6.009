# departments
departments = ('EECS', 'MATH', 'CHEM', 'HASS')

# subjects: 2 level one, 4 level 2, 8 level 3 for each department
subjects = [dept + '.' + num
            for dept in departments
            for num in ('10','11','20','21','22','23','31','32','33','34','35','36','37','38')]

# subject choices.  
# first dept is twice as likely as others
prest = 1.0/(len(departments) + 1)
pfirst = 2*prest

# build subject_choices list weighting number of entries for each subject
# by it's probability
subjects = []
subject_choices = []
for i,dept in enumerate(departments):
    for level in xrange(1,4):   # 3 levels
        # 2 level one, 4 level 2, 8 level 3 for each department
        nsubjs = {1: 2, 2:3, 3: 8}[level]
        for num in xrange(1,nsubjs+1):
            subject = "%s.%d%d" % (dept,level,num)
            subjects.append(subject)
            # level probabilities by level are in the ratio 4:2:1
            copies = {1: 4, 2: 2, 3: 1}[level]
            if i==0: copies *= 2   # first dept is twice as likely as others
            subject_choices.extend([subject]*copies)
                
def choose_n(n,seq):
    result = []
    while len(result) < n:
        choice = random.choice(seq)
        if not choice in result:
            result.append(choice)
    return result

def nsubjects():
    # 5% 3 courses
    # 10% 5 courses
    # 3% 6 courses
    # else 4 courses
    r = random.random()
    if r < 0.05: return 3
    elif r < 0.15: return 5
    elif r < 0.18: return 6
    else: return 4

# now build student records
records = [{"student": student,
            "subjects": choose_n(nsubjects(),subject_choices)}
           for student in students]

with open('records.json','w') as f:
    json.dump(records,f)

    
